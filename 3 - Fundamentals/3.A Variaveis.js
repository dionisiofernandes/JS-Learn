// Criar variaveis


// var

var learning = 'JavaScript';
var dias = 99;

// restrições: uma var nunca deve começar com um num ou caracter especial 
//Usar camelCase para nomear as var
var firstName = 'Luís Claudino';
console.log(firstName);
// pode ser com dupli ""
var firstName = "Luís Claudino";
console.log(firstName);
/// pode ser iniciada sem passar qualquer valor
var firstName;
console.log(firstName);
// pode passar mais que uma var de uma vez só separado por , 
var firstName = 'Luis Claudido',
    lastName = 'Dionísio Fernandes';

console.log(firstName);
console.log(lastName);
//let
//basicamente igual ao var mas sem a possibilidade de re iniciar a variavel

let minhaFamilia = ['Luis', 'Maria', 'Soraia'];
console.log(minhaFamilia);

// const 

// temos de definir um valor e não pode ser alterado - é uma constante

const nome = 'Luis';
console.log(nome);